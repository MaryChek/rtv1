#include "libft.h"

int		ft_atoi(const char *nptr)
{
	int					sign;
	long long int		res;
	int					i;
	long long int		x;

	sign = 1;
	res = 0;
	i = 0;
	while ((nptr[i] >= 9 && nptr[i] <= 13) || nptr[i] == 32)
		i++;
	if (nptr[i] == '-')
		sign = -1;
	if (nptr[i] == '-' || nptr[i] == '+')
		i++;
	while (nptr[i] && nptr[i] >= '0' && nptr[i] <= '9')
	{
		x = res * 10 + (nptr[i++] - '0');
		if (x < res)
			return (sign > 0 ? -1 : 0);
		res = x;
	}
	return (res * sign);
}
