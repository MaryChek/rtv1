#include "libft.h"

size_t		ft_cnt_w(char const *s, char c)
{
	size_t	w;

	w = 0;
	if (s != NULL)
	{
		while (*s)
		{
			if (*s != c && ((*(s + 1) == c) || (*(s + 1) == '\0')))
				w++;
			s++;
		}
	}
	return (w);
}
